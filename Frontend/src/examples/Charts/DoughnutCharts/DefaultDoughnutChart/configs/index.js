// Soft UI Dashboard React base styles
import colors from "assets/theme/base/colors";

const { gradients, primary } = colors;

function configs(labels, datasets, cutout = 60) {
  let backgroundColors = [];

  const dataset = datasets[0];
  if (dataset.customColors && dataset.customColors.length > 0) {
    backgroundColors = dataset.customColors;
  } else if (dataset.backgroundColors) {
    dataset.backgroundColors.forEach((color) => {
      if (gradients[color]) {
        if (color === "info") {
          backgroundColors.push(gradients.info.main);
        } else {
          backgroundColors.push(gradients[color].state);
        }
      } else {
        backgroundColors.push(primary.main);
      }
    });
  } else {
    backgroundColors.push(primary.main);
  }


  return {
    data: {
      labels,
      datasets: [
        {
          label: dataset.label,
          weight: 9,
          cutout,
          tension: 0.9,
          pointRadius: 2,
          borderWidth: 2,
          backgroundColor: backgroundColors,
          fill: false,
          data: dataset.data,
        },
      ],
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      plugins: {
        legend: {
          display: false,
        },
      },
      interaction: {
        intersect: false,
        mode: "index",
      },
      scales: {
        y: {
          grid: {
            drawBorder: false,
            display: false,
            drawOnChartArea: false,
            drawTicks: false,
          },
          ticks: {
            display: false,
          },
        },
        x: {
          grid: {
            drawBorder: false,
            display: false,
            drawOnChartArea: false,
            drawTicks: false,
          },
          ticks: {
            display: false,
          },
        },
      },
    },
  };
}

export default configs;